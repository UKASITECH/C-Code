#include<stdio.h>

int main(){
    // %d for Integers - int a = 1;
    int i  = 10;
    printf("%d\n", i);
    // %f for real values (floating-point numbers) float a = 4.4;
    float num = 1.1;
    printf("%f\n", num);
    // %c for chracters - char a = 'b';
    char a = 'A';
    printf("%c\n", a);
    return 0;
}